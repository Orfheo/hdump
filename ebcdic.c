void initialize_code_pages (void) {

/* This horrid little routine checks that there are 8-bit bytes and initializes
the code pages. */

    if (ebcdic_to_ascii != NULL) return;
    if (CHAR_BIT != 8 || UCHAR_MAX != 255) {
        fprintf(stderr,"The MVS suit of programs must have 8-bit bytes\n");
        abort();
    }
    code_pages[0].name = "phx";
    code_pages[0].table = phx_page;
    code_pages[1].name = "phx8";
    code_pages[1].table = phx8_page;
    code_pages[2].name = "phxmvt";
    code_pages[2].table = phx_mvt_page;
    code_pages[3].name = "ibmmvs";
    code_pages[3].table = ibm_mvs_page;
    code_pages[4].name = "ibm037";
    code_pages[4].table = code_37_page;
    code_pages[5].name = "ibm500";
    code_pages[5].table = code_500_page;
    code_pages[6].name = NULL;
    code_pages[6].table = NULL;
    ebcdic_to_ascii = code_pages[0].table;
}



CODE_PAGE *select_code_page (const char *name) {

/* This selects a code page, and returns the first one if the name is NULL
or the empty string. */

    CODE_PAGE *ptr;
    int i;

    initialize_code_pages();
    ptr = &code_pages[0];
    ebcdic_to_ascii = ptr->table;
    if (name == NULL || name[0] == '\0') return ptr;
    while (ptr->name != NULL) {
        for (i = 0; toupper(name[i]) == toupper(ptr->name[i]); ++i)
            if (name[i] == '\0') {
                ebcdic_to_ascii = ptr->table;
                return ptr;
            }
        ++ptr;
    }
    return NULL;
}
